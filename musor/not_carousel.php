		Происходит из итальянского языка, где означает церковная паперть. Галереи в виде дворцовых залов с одной стеной в виде сплошного ряда больших окон появились в начале XVI в. 
		и особенно распространились в стилевую эпоху барокко. Противоположная окнам глухая, как правило, стена, использовалась для размещения произведений живописи и скульптуры, 
		что давало хорошее естественное освещение. Удобство размещения экспозиций в галереях привело к тому, что галереями стали называть художественные музеи или их части вообще, 
		без привязки к архитектурным особенностям. Современная архитектура продолжает использовать галереи, но их активно стали вытеснять онлайн галереи, 
